class primitive
{
    public static void main(String args[])
    {
        byte b=12;
        int a=520;
        short c =778;
        long l=123456789;
        float f=10.2f;
        double d=150.65;
        char ch='A';
        char cha='$';
        System.out.println(b);
        System.out.println(a);
        System.out.println(c);
        System.out.println(f);
        System.out.println(d);
        System.out.println(ch);
        System.out.println(cha);
        System.out.println(l);
    }
}